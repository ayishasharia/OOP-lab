class Operations{
	public static void main(String args[]){
		int num1=50,num2=10;
		System.out.println("The first number is "+num1);
		System.out.println("The second number is "+num2);
		System.out.println(num1 + "+" + num2 + "=" + (num1+num2));
		System.out.println(num1 + "-" + num2 + "=" + (num1-num2));
		System.out.println(num1 + "*" + num2 + "=" + (num1*num2));
		System.out.println(num1 + "/" + num2 + "=" + (num1/num2));
	}
}